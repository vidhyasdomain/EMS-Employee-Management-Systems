package com.employee;

public class EmployeeTable {

	String Name;
	int id;
	int dpt_id;
	int age;
	int salary;
	
	public EmployeeTable(String name, int id, int dpt_id, int age, int salary) {
		super();
		Name = name;
		this.id = id;
		this.dpt_id = dpt_id;
		this.age = age;
		this.salary = salary;
	}
	
}
